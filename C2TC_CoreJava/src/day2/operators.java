package day2;

public class operators {
	public static void main(String[] args) {
		int a=12;
		int b= 10;
		System.out.println(a+b);
		System.out.println(a/b);
		System.out.println(a*b);
		
		//increment and decrement
		
		System.out.println(++a + b-- + a++);
		
		// ternary
		
		a=(a==12) ? 1 : 0;
		
		System.out.println(a);
		 
	}
}
