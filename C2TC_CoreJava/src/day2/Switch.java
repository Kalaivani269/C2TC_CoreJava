package day2;

public class Switch {
	public static void main(String[] args) {
		
		int a=5;
		switch(a)
		{
		case 1:
			System.out.println("odd");
			break;
		
		case 2:
			System.out.println("even");
			break;
		
		case 3:
			System.out.println("odd");
			break;
		case 4:
			System.out.println("even");
			break;
		case 5:
		System.out.println("odd");
		break;
		
		}
		
	}

}
